//built in values
// NaN (Not a Number)
let a = 10 / "abc";

// Infinity
let b = 10 / 0;

// Undefined
let c;

// Null
let d = null;

// Printing values and their types
console.log(`NaN value: ${a} | Type: ${typeof a}`);
console.log(`Infinity value: ${b} | Type: ${typeof b}`);
console.log(`Undefined value: ${c} | Type: ${typeof c}`);
console.log(`Null value: ${d} | Type: ${typeof d}`);